    public void habilitarCampos(boolean flag){
        txtInputNome.setEnabled(flag);
        txtInputIdade.setEnabled(flag);
        txtInputMatricula.setEnabled(flag);
        txtInputAno.setEnabled(flag);
        txtInputSexo.setEnabled(flag);
    }
    
    public void limparCampos(){
        txtInputNome.setText("");
        txtInputIdade.setText("");
        txtInputMatricula.setText("");
        txtInputAno.setText("");
        txtInputSexo.setText("");
    }
    
    public void camposParaObjeto(){
        Aluno a = new Aluno();
        a.setNome(txtInputNome.getText());
        int idade = Integer.parseInt(txtInputIdade.getText());
        a.setIdade(idade);
        a.setMatricula(txtInputMatricula.getText());
        a.setSexo(txtInputSexo.getText());
        a.setAno(txtInputAno.getText());
        
        lista.add(a);
    }
    
    public String imprimirParaString(){
        return "Hello World!";
    }
    public void imprimirListaAlunos(){
        String listagemTotal = " ";
        for(int i = 0; i <= lista.size() - 1; i++){
            listagemTotal = listagemTotal + lista.get(i);
        }
        System.out.println(listagemTotal);
    }
    
